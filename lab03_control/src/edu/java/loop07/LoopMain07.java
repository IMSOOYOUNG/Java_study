package edu.java.loop07;

public class LoopMain07 {

    public static void main(String[] args) {
        // do-while 반복문
        
        int n = 1; 
        do {
            System.out.print(n + " ");
            n++;
         }while(n <= 5);

        // while 문은 반복을 시작하기 전에 (반복) 조건을 먼저 검사하기 때문에,
        // 실행 블록({...})이 한 번도 실행되지 않는 경우가 있을 수 있음.
        
        
    }

}
